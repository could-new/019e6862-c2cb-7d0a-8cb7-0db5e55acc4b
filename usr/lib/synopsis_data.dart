## 1. प्रस्तावना (Introduction)

शिक्षा किसी भी समाज के विकास की आधारशिला है और विद्यालय वह पवित्र स्थल है जहाँ बच्चों का भविष्य गढ़ा जाता है। इस प्रक्रिया में शिक्षक की भूमिका केवल एक मार्गदर्शक या ज्ञान प्रदाता की नहीं होती, बल्कि वह बच्चों के संरक्षक (Guardian) और रक्षक की भी भूमिका निभाता है। वर्तमान परिदृश्य में, जहाँ बच्चों के विरुद्ध होने वाले अपराधों, विशेषकर यौन शोषण के मामलों में चिंताजनक वृद्धि हुई है, विद्यालयों और शिक्षकों का उत्तरदायित्व और भी अधिक बढ़ गया है। 

बच्चों को यौन अपराधों से बचाने और उन्हें एक सुरक्षित वातावरण प्रदान करने के उद्देश्य से भारत सरकार ने वर्ष 2012 में 'लैंगिक अपराधों से बालकों का संरक्षण अधिनियम' (Protection of Children from Sexual Offences Act - POCSO) लागू किया। यह अधिनियम न केवल बच्चों के खिलाफ होने वाले यौन शोषण, यौन उत्पीड़न और अश्लील साहित्य (Pornography) के अपराधों को परिभाषित करता है, बल्कि यह बाल-सुलभ न्याय प्रक्रिया भी सुनिश्चित करता है। 

विद्यालय, जहाँ बच्चे अपने दिन का एक बड़ा हिस्सा व्यतीत करते हैं, वहां सुरक्षा का वातावरण सुनिश्चित करना सर्वोच्च प्राथमिकता होनी चाहिए। माध्यमिक स्तर (Secondary Level) के विद्यार्थी किशोरावस्था (Adolescence) के दौर से गुजर रहे होते हैं, जो शारीरिक और मानसिक दोनों ही दृष्टियों से अत्यंत संवेदनशील अवस्था होती है। इस उम्र में बच्चे कई बार अपने साथ होने वाली अनुचित घटनाओं को समझ नहीं पाते या डर और शर्म के कारण किसी को बता नहीं पाते। ऐसे में शिक्षकों का जागरूक होना अत्यंत आवश्यक है। यदि शिक्षक पॉक्सो (POCSO) अधिनियम के प्रावधानों, अपराध के लक्षणों, रिपोर्टिंग की प्रक्रिया और बच्चों के प्रति अपने विधिक एवं नैतिक उत्तरदायित्वों से भली-भांति अवगत होंगे, तो वे न केवल संभावित अपराधों को रोक सकेंगे, बल्कि पीड़ित बच्चों को उचित न्याय और सहायता भी दिला सकेंगे।

दुर्भाग्यवश, कई अध्ययनों और समाचार रिपोर्टों से यह तथ्य सामने आता है कि स्वयं विद्यालयों में या शिक्षकों के आस-पास बच्चों के साथ दुर्व्यवहार की घटनाएं होती हैं, और कई बार जानकारी के अभाव में या विद्यालय की बदनामी के डर से इन घटनाओं को दबा दिया जाता है। पॉक्सो अधिनियम की धारा 19 के तहत यह स्पष्ट प्रावधान है कि यदि किसी व्यक्ति को यह आशंका है या उसे जानकारी मिलती है कि किसी बच्चे के साथ यौन अपराध हुआ है, तो उसे तुरंत इसकी सूचना स्थानीय पुलिस या विशेष किशोर पुलिस इकाई (SJPU) को देनी होगी। सूचना न देना भी इस अधिनियम के तहत एक दंडनीय अपराध है (धारा 21)। 

इस संदर्भ में, माध्यमिक स्तर के विद्यालयों के शिक्षकों की पॉक्सो अधिनियम के प्रति जागरूकता का आकलन करना एक महत्वपूर्ण शोध का विषय बन जाता है। क्या हमारे शिक्षक इस बात से अवगत हैं कि कानूनी रूप से 'बच्चा' (Child) कौन है? क्या वे 'गुड टच' (Good Touch) और 'बैड टच' (Bad Touch) के बारे में बच्चों को शिक्षित करने में सक्षम हैं? क्या उन्हें मालूम है कि किसी घटना का संदेह होने पर उन्हें क्या कदम उठाने चाहिए? 

प्रस्तुत शोध अध्ययन इसी दिशा में एक प्रयास है। यह अध्ययन विशेष रूप से माध्यमिक स्तर के विद्यालयों में कार्यरत शिक्षकों की पॉक्सो अधिनियम, 2012 के विभिन्न विधिक प्रावधानों, रिपोर्टिंग तंत्र और दंड संबंधी नियमों के प्रति उनकी समझ और जागरूकता का विश्लेषणात्मक और वर्णनात्मक (Descriptive) मूल्यांकन करेगा। इसके साथ ही, यह अध्ययन यह भी जानने का प्रयास करेगा कि क्या शिक्षकों की शैक्षिक योग्यता, उनका अनुभव, या विद्यालय का प्रकार (सरकारी/निजी) उनकी जागरूकता के स्तर को प्रभावित करता है।

यह शोध न केवल वर्तमान स्थिति का यथार्थ चित्रण प्रस्तुत करेगा, बल्कि शिक्षा विभाग और नीति-निर्माताओं को शिक्षकों के लिए उपयुक्त प्रशिक्षण कार्यक्रम (Training Modules) और कार्यशालाओं (Workshops) की रूपरेखा तैयार करने में भी सहायता करेगा, ताकि विद्यालयों को वास्तव में बच्चों के लिए एक सुरक्षित आश्रय स्थल (Safe Haven) बनाया जा सके।

